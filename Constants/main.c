#include <stdio.h>
#include <stdlib.h>

int main()
{
    // Normal Variable
    int num = 5;
    printf("%d\n",num);
    num = 8;
    printf("%d\n",num);

    // Constant Variable (For best practice write constant variable in capital letter)
    const int NUMBER = 4;
    printf("%d\n",NUMBER);

    printf("Hello");
    printf("%d", 90);

    return 0;
}
