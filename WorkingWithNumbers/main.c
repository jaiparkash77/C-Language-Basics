#include <stdio.h>
#include <stdlib.h>

int main()
{
    printf("%f\n", 8.9);
    //Math operations
    printf("%f\n", 2.4 + 3.5);
    printf("%f\n", 5 + 4.5);

    //Math Functions
    printf("%f\n", pow(4,3));   // Power
    printf("%f\n", sqrt(36));   // Square Root
    printf("%f\n", ceil(36.356));   // Higher number
    printf("%f\n", floor(36.656));  // Lower number


    return 0;
}
